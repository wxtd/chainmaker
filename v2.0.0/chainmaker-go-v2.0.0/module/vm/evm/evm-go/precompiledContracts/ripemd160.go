package precompiledContracts

import (
	"chainmaker.org/chainmaker-go/evm/evm-go/params"
	"chainmaker.org/chainmaker/common/v2/evmutils"
	"golang.org/x/crypto/ripemd160"
)

// RIPEMD160 implemented as a native contract.
type ripemd160hash struct{}

//func (c *ripemd160hash)SetValue(v string){}
// RequiredGas returns the gas required to execute the pre-compiled contract.
//
// This method does not require any overflow checking as the input size gas costs
// required for anything significant is so high it's impossible to pay for.
func (c *ripemd160hash) GasCost(input []byte) uint64 {
	return uint64(len(input)+31)/32*params.Ripemd160PerWordGas + params.Ripemd160BaseGas
}
func (c *ripemd160hash) Execute(input []byte) ([]byte, error) {
	ripemd := ripemd160.New()
	ripemd.Write(input)
	return evmutils.LeftPadBytes(ripemd.Sum(nil), 32), nil
}
